const STATIC_CACHE = 'agi-static-v1.0.0';
const DYNAMIC_CACHE = 'agi-dynamic-v1.0.0';

// Files to cache immediately
const STATIC_FILES = [
  '/',
  '/index.html',
  '/manifest.json',
  '/favicon.ico',
  '/icon-192.png',
  '/icon-512.png'
];

